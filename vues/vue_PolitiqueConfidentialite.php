    <h1>Politique de confidentialité</h1>
    
    <h2>Collecte et utilisation des informations</h2>
    <p>Nous collectons certaines informations personnelles lors de votre utilisation de notre plateforme, notamment votre pseudo, votre adresse e-mail et votre mot de passe. Ces informations sont utilisées dans le cadre de la gestion de votre compte et de votre participation aux équipes de gaming affiliées à La Nouvelle Lignée. Nous ne partageons pas ces informations avec des tiers sans votre consentement explicite.</p>
    
    <h2>Protection des données</h2>
    <p>Nous accordons une grande importance à la sécurité de vos données personnelles. Nous mettons en place des mesures de sécurité appropriées pour protéger vos informations contre tout accès non autorisé, toute divulgation, toute altération ou toute destruction.</p>
    
    <h2>Accès à vos informations</h2>
    <p>Vous avez le droit d'accéder, de corriger et de supprimer vos informations personnelles. Vous pouvez le faire en vous connectant à votre compte sur notre plateforme ou en nous contactant directement.</p>
    
    <h2>Modifications de notre politique de confidentialité</h2>
    <p>Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Toute modification sera publiée sur cette page.</p>
    
    <p>Merci de consulter régulièrement notre politique de confidentialité pour vous tenir informé des éventuelles mises à jour.</p>
